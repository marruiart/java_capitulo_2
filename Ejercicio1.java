public class Ejercicio1 {
    public static void main(String args[]) {
        int x = 144;
        int y = 999;
        System.out.printf("x = %d | y = %d\n", x, y);
        System.out.printf("x + y = %d\n", x + y);
        System.out.printf("x - y = %d\n", x - y);
        System.out.printf("x * y = %d\n", x * y);
        System.out.printf("x / y = %.4f\n", (float)x / (float)y);
    }
}
