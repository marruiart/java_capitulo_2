/**
 * This program shows how to print 'char' variables altogether with String ones
 * 
 * @autor Marina Ruiz
 */

public class Ejercicio8 {
    public static void main(String args[]) {
        char a = 'a';
        char m = 'm';
        char i = 'i';
        char g = 'g';
        char o = 'o';
        String cadena = "" + a + m + i + g + o;
        System.out.println(cadena);
    }
}
